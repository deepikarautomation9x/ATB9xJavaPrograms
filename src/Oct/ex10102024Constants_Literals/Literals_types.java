package Oct.ex10102024Constants_Literals;

public class Literals_types {
    public static void main(String[] args) {
          /* ++++++ LITERALS - Assigned values to a variable++++++++
    Types of literals */
        boolean b = true ; //Boolean Literals
        float a = 2.2f ; // Decimal literals
        int ABC = 0576 ; // Octa Literals -PREFIX WITH 0
        int x = 0X123Face; // hexa decimal literals
        int Y = 0b1111; //Binary literals - prefix with Ob or 0B
        char ch = 'H' ; //single character literal within single quote
        char dh = 0555; //It is an Integer literal with octal form
        char eh = '\u0061'; // Here /u0061 represent a.Unicode representation '/uxxxx'
        char fh = '\n'; //Escape Literals
        String s = "Hello"; //String literals
        System.out.println(b);
        System.out.println(a);
        System.out.println(x);
        System.out.println(Y);
        System.out.println(ch);
        System.out.println(eh);
        System.out.println(fh);
        System.out.println(s);
    }


}
